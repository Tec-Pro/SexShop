    private void okButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okButtonActionPerformed
        LinkedList<Pair> precioNuevoId= new LinkedList<Pair>();
        for(int i=0;i<tablaProductos.getRowCount();i++){
           if(!tablaProductos.getValueAt(i, 5).equals(tablaProductos.getValueAt(i, 6))) {
               Pair par= new Pair(tablaProductos.getValueAt(i, 0),tablaProductos.getValueAt(i, 6));
               precioNuevoId.add(par);
           }
        }
        productosModificados= precioNuevoId;
        doClose(RET_OK);
    }//GEN-LAST:event_okButtonActionPerformed
    
