    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int[] aModificar = tablaProductos.getSelectedRows();
                    for(int i=0;i<aModificar.length;i++){
                        BigDecimal precioViejo=(BigDecimal) tablaProductos.getValueAt(aModificar[i], 5);
                        BigDecimal cien= new BigDecimal(100);
                        BigDecimal porcentajeBigDecimal= BigDecimal.valueOf(((Integer)porcentaje.getValue()).doubleValue());
                        tablaProductos.setValueAt(precioViejo.add(precioViejo.multiply(porcentajeBigDecimal).divide(cien)).setScale(2, RoundingMode.CEILING), aModificar[i], 6);
                    }

    }//GEN-LAST:event_jButton1ActionPerformed
    
